package UASPBO2023.Model;

public class Game {
    private int id, price;
    private String name, genre;

    public Game() {
    }

    public Game(int id, int price, String name, String genre) {
        this.id = id;
        this.price = price;
        this.name = name;
        this.genre = genre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }
}
